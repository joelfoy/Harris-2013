<h3><a href="#" class="toggle-buttton">+</a>Where can I find the outbound links/downloads that are clicked?</h3>
<div style="display: none">Open your analytics and click in the left hand side menu on <strong>Content</strong> > <strong>Event Tracking</strong>. Here you can see an overview of outbound links/downloads that are clicked.</div>

<h3><a href="#" class="toggle-buttton">+</a>There are no click tracking results visible</h3>
<div style="display: none">Make sure to wait a few days because sometimes it takes a few days before Google picks up the event tracking.</div>
